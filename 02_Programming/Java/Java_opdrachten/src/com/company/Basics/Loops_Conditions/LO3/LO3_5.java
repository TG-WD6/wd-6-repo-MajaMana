package com.company.Basics.Loops_Conditions.LO3;

public class LO3_5 {
    public static void main(String[] args) {
        for (int l = 0; l < 3; l++) {
            for (int i = 1; i < 11; i++) {
                System.out.println(i);
            }
            for (int f = 2; f < 22; f += 2) {
                System.out.println(f);
            }
            for (int g = 4; g < 44; g += 4) {
                System.out.println(g);
            }
        }
    }
}
