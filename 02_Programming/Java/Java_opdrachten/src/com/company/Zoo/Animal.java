package com.company.Zoo;

public class Animal {
    public String name;
    public String environment;
    public int feedingFrequency;
    public int feedingAmount;

    public Animal(String name, String environment, int feedingFrequency, int feedingAmount) {
        this.name = name;
        this.environment = environment;
        this.feedingFrequency = feedingFrequency;
        this.feedingAmount = feedingAmount;
    }
}
